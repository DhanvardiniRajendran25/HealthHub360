    // Utility method to get role ID
    private int getRoleId(String roleName) {
        switch (roleName) {
            case "Doctor":
                return 3;
            case "Patient":
                return 4;
            case "Lab Manager":
                return 5;
            case "Lab Technician":
                return 6;
            case "Pharmacy Manager":
                return 7;
            case "Pharmasist":
                return 8;
            case "Insurance Manager":
                return 9;
            case "Billing Manager":
                return 10;
            default:
                return -1; // Invalid role
        }
    }
