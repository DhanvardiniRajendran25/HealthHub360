    private void BtnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnSaveActionPerformed
        try {
            if (TxtName.getText().trim().isEmpty() || TxtUserName.getText().trim().isEmpty()
                    || TxtPassword.getText().trim().isEmpty() || TxtContact.getText().trim().isEmpty()
                    || TxtEmail.getText().trim().isEmpty() || TxtAdd.getText().trim().isEmpty()) {
                JOptionPane.showMessageDialog(this, "All fields are required!", "Validation Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            String name = TxtName.getText().trim();
            String contact = TxtContact.getText().trim();
            String email = TxtEmail.getText().trim();
            String address = TxtAdd.getText().trim();
            String username = TxtUserName.getText().trim();
            String password = TxtPassword.getText().trim();
            String roleName = (String) CmbRole.getSelectedItem();

            if (!email.matches("^[A-Za-z0-9+_.-]+@[A-Za-z0-9.-]+$")) {
                JOptionPane.showMessageDialog(this, "Invalid email format!", "Validation Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
            if (!contact.matches("\\d{10}")) {
                JOptionPane.showMessageDialog(this, "Invalid contact number. It should be 10 digits!", "Validation Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
            if (userDAO.usernameExists(username)) {
                JOptionPane.showMessageDialog(this, "Username already exists. Please choose another one!", "Validation Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
            int roleId = getRoleId(roleName);
            if (roleId == -1) {
                JOptionPane.showMessageDialog(this, "Invalid role selected!", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            boolean isSuccess = userDAO.addUser(username, password, roleId, name, contact, email, address);

            if (isSuccess) {
                JOptionPane.showMessageDialog(this, "User added successfully!", "Success", JOptionPane.INFORMATION_MESSAGE);
                success = true;
                dispose(); 
            } else {
                JOptionPane.showMessageDialog(this, "Failed to add user.", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } catch (SQLException e) {
            LOGGER.severe("Database error: " + e.getMessage());
            JOptionPane.showMessageDialog(this, "Database error: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
        // TODO add your handling code here:
    }//GEN-LAST:event_BtnSaveActionPerformed

